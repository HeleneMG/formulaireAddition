function addition() {
    let nb1 = parseInt(document.getElementById("nb1").value);
    let nb2 = parseInt(document.getElementById("nb2").value);
    let result = document.getElementById("result");
    result.value = nb1 + nb2;
}
function soustraction() {
    let nb1 = parseInt(document.getElementById("nb1").value);
    let nb2 = parseInt(document.getElementById("nb2").value);
    let result = document.getElementById("result");
    result.value = nb1 - nb2;
}